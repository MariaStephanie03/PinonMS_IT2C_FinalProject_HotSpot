                <h3>Nakji-bokkeum 낙지볶음</h3>
                <p>Stir-fried octopus </p>
                <p><strong>Ingredients:</strong><br>
                    <em>1.</em> 1 package (1½ pounds) of frozen long arm octopus, thawed out in the refrigerator<br>
                    <em>2.</em> 2 tablespoons flour (or salt)<br>
                    <em>3.</em> 7 garlic cloves, minced<br>
                    <em>4.</em> 2 teaspoons minced ginger<br>
                    <em>5.</em> 1 Korean spicy green chili pepper (or jalapeño), chopped<br>
                    <em>6.</em> 4 ounces onion, chopped<br>
                    <em>7.</em> 1 large Korean green onion (or 4 green onions), chopped<br>
                    <em>8.</em> 2 to 3 tablespoons vegetable oil<br>
                    <em>9.</em> 1 teaspoon toasted sesame oil<br></p>

                <p><strong>For seasonings:</strong><br>
                    <em>1.</em> ¼ cup Korean hot pepper flakes (gochugaru)<br>
                    <em>2.</em> 3 tablespoons soy sauce<br>
                    <em>3.</em> 1 tablespoon sugar<br>
                    <em>4.</em> 1 tablespoon potato starch (or cornstarch)<br>
                    <em>5.</em> ¼ cup rice syrup<br>
                    <em>6.</em> ½ teaspoon ground black pepper<br></p>

                <p><strong>Instructions:<br>
                            How to handle the nakji:</strong><br>
                    <em>1.</em> Cut open the heads and remove the intestines and beaks.<br>
                    <em>2.</em> Put in a large bowl. Add flour and scrub vigorously for about 2 minutes until it foams. This process not only tenderizes the octopus but also cleans any dirt or mud stuck in the suckers.<br>
                    <em>3.</em> Rinse in cold running water until it’s not slippery anymore. Drain.<br>
                    <em>4.</em> Bring a pot of water to a boil. Blanch the octopus for 10 to 12 seconds, drain, and then transfer to a cutting board.<br>
                    <em>5.</em> Cut the heads and arms into bite size pieces. Set aside.</p>

                <p><strong>Make seasoning sauce:</strong><br>
                    <em>1.</em> Combine all the seasoning ingredients (hot pepper flakes, soy sauce, sugar, potato starch, rice syrup and ground black pepper) in a small bowl.<br>
                    <em>2.</em> Mix well with a spoon.<br></p>

                <p><strong>Stir-fry:</strong><br>
                    <em>1.</em> Heat up a large pan over high heat. Add vegetable oil.<br>
                    <em>2.</em> Stir in garlic, ginger, green chili pepper, onion, and green onion for about 3 minutes until the garlic turns a little brown and the onion translucent.<br>
                    <em>3.</em> Add nakji and seasoning sauce. Stir for 2 to 3 minutes.<br>
                    <em>4.</em> Remove from the heat and stir in sesame oil. Transfer it to a serving plate and serve right away with rice.</p>
