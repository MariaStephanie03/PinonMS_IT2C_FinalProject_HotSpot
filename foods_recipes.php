                <h3>Maeundae galbijjim 갈비찜</h3>
                <p>Braised short-rib Stew</p>
                <p><strong>Ingredients:</strong><br>

                    <em>1.</em> 15 lb 1” to 1 1/2” thick single beef short ribs<br>
                    <em>2.</em> 3/4 cup soy sauce<br>
                    <em>3.</em> 1/3 cup sugar<br>
                    <em>4.</em> 1 cup soju, Korea liquor or rice wine<br>
                    <em>5.</em> 3 to 6 tbsp gochugaru Korean red pepper flakes or regular red pepper flakes (for extra spiciness, add Thai chili flakes)<br>
                    <em>6.</em> 1/4 cup maesilaek, Korean plum extract or apple/pear juice<br>
                    <em>7.</em> 1 tsp black pepper<br>
                    <em>8.</em> 20 cloves garlic, chopped<br>
                    <em>9.</em> 1 1/2 tsp chopped ginger<br>
                    <em>10.</em> 6 cups water<br>
                    <em>11.</em> 1 1/2 lb moo, Korean radish or Yukon gold potatoes, peeled, cut into 2”x2” cubes<br>
                    <em>12.</em> 2 large carrots, peeled, cut into 2”x2” cubes<br>
                    <em>13.</em> 2 large yellow or sweet onion, cut into 2”x2” cubes<br>
                    <em>14.</em> 4 to 6 oz dried dangmyeon Korean glass noodles or zero calorie noodles (optional), soaked in hot water for 30 minutes, drained right before use<br>
                    <em>15.</em> 5 to 6 green onions, cut into 2” longe pieces<br>
                    <em>16.</em> 1 to 3 green and/or red chili, sliced</p>

                    <p><strong>Instructions:</strong><br>

                        <em>1.</em>  Bring enough water to boil that can cover all the beef short ribs in a large pot. Add the beef short ribs. Let the water bring back to boil, about 8 to 10 minutes. When it started boiling again, drain the water and beef in a strainer. You could save the broth, but it is filled with beef blood and bone bits, so make sure to strain in a super fine kitchen cloth. Quickly rinse the pot. Wash the beef one by one under running cold water. Add the clean beef short ribs in the now-clean pot.<br> 

                        <em>2.</em>  Add soy sauce, sugar, soju, gochugaru, maesilaek, black pepper, garlic ginger and water. (I added garlic and ginger at last in the video, because I forgot to add in the begining! lol) Mix well, cover and bring it to boil over high heat. Let it hard boil for 30 minutes over high heat then reduce heat to medium and simmer for additional 30 minutes.<br>

                        <em>3.</em>  Meanwhile, smooth edges of Korean radish and carrot. This step is not necessary for the taste, it’s just for the look. By treating the sharp edge of the vegetables will prevent break down and make the galibijjim looks not as neath.<br>

                        <em>4.</em>  Add the radish and carrot into the pot. Stir and cook additional 30 minutes. Then add the onion and soaked glass noodles (if you are using). Cook 5 to 10 more minutes or until onions are soften and the noodles are fully cooked. Remove from heat, stir in green onions and chili. Transfer to a serving plate and garnish with sesame seeds. Enjoy with a warm bowl of rice and kimchi!</p>
