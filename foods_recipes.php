                <h3>Tteokbokki 떡볶이</h3>
                <p>Hot and spicy rice cake</p>
                <p><strong>Ingredients: (MAIN)</strong><br>
                    <em>1.</em> 350g / 12 ounces Korean rice cakes, separated<br>
                    <em>2.</em> 150g / 5.3 ounces Korean fish cakes, rinsed over hot water & cut into bite size pieces<br>
                    <em>3.</em> 2 cups Korean soup stock (dried kelp and dried anchovy stock), use this recipe<br>
                    <em>4.</em> 60g / 2 ounces onion, thinly sliced</p>

                <p><strong>TTEOKBOKKI SAUCE:</strong><br>
                    <em>1.</em> 3 Tbsp gochujang (Korean chili paste)<br>
                    <em>2.</em> 1 1/2 Tbsp raw sugar<br>
                    <em>3.</em> 1 Tbsp soy sauce<br>
                    <em>4.</em> 1 tsp minced garlic<br>
                    <em>5.</em> 1 tsp gochugaru (Korean chili flakes)</p>

                <p><strong>GARNISH:</strong><br>
                    <em>1.</em> 1 tsp roasted sesame seeds<br>
                    <em>2.</em> 1 tsp sesame oil<br>
                    <em>3.</em> 1 stalk green onion, finely chopped</p>

                <p><strong>Instructions;</strong><br>
                    <em>1.</em> Unless your rice cakes are soft already, soak them in warm water for 10 mins.<br>
                    <em>2.</em> Boil the soup stock in a shallow pot over medium high heat and dissolve the tteokbokki sauce by stirring it with a spatula. Once the seasoned stock is boiling, add the rice cakes, fish cakes and onion. Boil them a further 3 to 5 mins until the rice cakes are fully cooked. Then, to thicken the sauce and to deepen the flavor, simmer it over low heat for a further 2 to 4 mins.<br>
                    <em>3.</em> Add the sesame oil, sesame seeds, and green onion then quickly stir. Serve warm.<br></p>
