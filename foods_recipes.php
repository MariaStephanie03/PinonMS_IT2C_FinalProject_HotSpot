                <h3>Jjambbong</h3>
                <p>Korean Seafood Noodle Soup</p>
                <p><strong>Ingredients: (For Stock)</strong><br>
                    <em>1.</em> 1 ounce of large dried anchovies (about 24 anchovies) with the heads and guts removed<br>
                    <em>2.</em> 1 piece (about 6×6 inch) dried kelp<br>
                    <em>3.</em> 12 cups water</p>

                <p><strong>Vegetables:</strong><br>
                    <em>1.</em> 1 daepa (large green onion), or 4 green onions, cut into 2 inch length<br>
                    <em>2.</em> 2 ounces leek, washed and cut into ½ x 2 inch strips<br>
                    <em>3.</em> 4 ounces bok choy, washed<br>
                    <em>4.</em> 3 large cabbage leaves (about 3 ounces), cut into bite-size pieces<br>
                    <em>5.</em> 4 ounces onion, sliced<br>
                    <em>6.</em> 1 small carrot, peeled and cut into 2-inch strips</p>

                <p><strong>Seafoods and meat:</strong><br>
                    <em>1.</em> 8 mussels, scrubbed, debearded, soaked in salted water for a few hours, and washed<br>
                    <em>2.</em> 4 large shrimp, shelled and deveined<br>
                    <em>3.</em> 4 ounces squid, just the body with guts removed and sliced into rings<br>
                    <em>4.</em> 24 small clams (optional), soaked in salted water for a few hours, and washed<br>
                    <em>5.</em> 4 ounces of thinly sliced beef (or pork or chicken), cut into bite-size pieces</p>

                <p><strong>Noodles and seasonings:</strong><br>
                    <em>1.</em> 2 bunches (1 pound) fresh or frozen jjajangmyeon noodles, thawed if frozen, and uncoiled<br>
                    <em>2.</em> 5 tablespoons gochugaru (Korean hot pepper flakes)<br>
                    <em>3.</em> 1 teaspoon toasted sesame oil<br>
                    <em>4.</em> 1 tablespoon minced garlic<br>
                    <em>5.</em> 1 teaspoon minced ginger<br>
                    <em>6.</em> ¼ cup vegetable oil<br>
                    <em>7.</em> 1 tablespoon fish sauce<br>
                    <em>8.</em> 1 teaspoon kosher salt</p>

                <p><strong>Instructions: Make Stock</strong><br>
                    <em>1.</em> Combine the water, anchovies, and kelp in a large pot. Cover, and cook over medium-high heat for 20 minutes.<br>
                    <em>2.</em> Reduce the heat to low and cook for another 20 minutes.<br>
                    <em>3.</em> Strain the stock and you will have 8 to 10 cups’ worth. Set aside.</p>

                <p><strong>Make the hot pepper flakes mixture:</strong><br>
                    <em>1.</em> Combine 2 tablespoons of hot pepper flakes and 1 teaspoon toasted sesame oil in a small bowl.<br>
                    <em>2.</em> Mix with a spoon until well incorporated. Set aside.</p>

                <p><strong>Make jjamppong:</strong><br>
                    <em>1.</em> Heat a large wok (or pot) over high heat. Add the vegetable oil, garlic, ginger, and stir about 20 seconds with a wooden spoon until the garlic starts to turn a little crispy.<br>
                    <em>2.</em> Add the beef and stir until slightly cooked.<br>
                    <em>3.</em> Clear a spot in the wok by pushing the garlic, ginger, and the meat to the side. Tilt the wok so that the excess vegetable oil slides into the cleared area. Put 3 tablespoons hot pepper flakes into the hot oil and stir and mix with the wooden spoon for about 1 minute, until it creates a smoky flavor but not long enough to burn. Then stir everything in the wok together into the hot oil.<br>
                    <em>4.</em> Add green onion, leek, cabbage, and onion and stir for 3 to 4 minutes until the vegetables are wilted.<br>
                    <em>5.</em> Add 6 cups stock and all the seafoods and bok choy. Cover and cook 7 to 8 minutes until the mussels and clams are open and the shrimp and squid are well cooked.<br>
                    <em>6.</em> Stir in the fish sauce, kosher salt, and the reserved hot pepper flakes mixture. Cover and let it simmer over low heat.</p>

                <p><strong>Meanwhile, cook the noodles:</strong><br>
                    <em>1.</em> Bring a large pot of water to a boil. Add the noodles and stir a few times so that they don’t stick to each other.<br>
                    <em>2.</em> Cover and cook 5 to 8 minutes until tender but still chewy.<br>
                    <em>3.</em> Strain and rinse the noodles in cold running water to make them nice and chewy.</p>

                <p><strong>Put it together and serve:</strong><br>
                    <em>1.</em> Heat up the soup over high heat.<br>
                    <em>2.</em> Divide the noodles into individual serving bowls. Add the soup over top and include cooked seafood, vegetables, meat over top of the noodles. Serve right away.</p>
