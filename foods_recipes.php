                <h3>Chikin</h3>
                <p>Korean Fried Chicken</p>
                <p><strong>Ingredients</strong><br>
                    <em>1.</em> 10 chicken wings, or drumettes<br>
                    <em>2.</em> 1 cup cornstarch(125 g)<br>
                    <em>3.</em> oil, for frying<br>
                    <em>4.</em> sesame seed, to serve</p>

                <p><strong>Batter:</strong><br>
                    <em>1.</em> ½ cup cornstarch(65 g)<br>
                    <em>2.</em> ½ cup flour(65 g)<br>
                    <em>3.</em> 2 teaspoons salt<br>
                    <em>4.</em> 1 cup water(240 mL)</p>

                <p><strong>Sauce:</strong><br>
                    <em>1.</em> 2 tablespoons soy sauce<br>
                    <em>2.</em> ¼ cup honey(85 g)<br>
                    <em>3.</em> 2 tablespoons rice vinegar<br>
                    <em>4.</em> 2 tablespoons gochujang, (Korean chili paste)<br>
                    <em>5.</em> 2 cloves garlic, minced<br>
                    <em>6.</em> 1 tablespoon ginger, grated</p>

                <p><strong>Instructions:</strong><br>
                    <em>1.</em> Mix the ingredients for the batter in a bowl until smooth with no lumps.<br>
                    <em>2.</em> Heat oil in a pot to 340°F (170°C).<br>
                    <em>3.</em> Dip a chicken wing into the cornstarch, shaking off excess, then dredge the wing into the batter, dripping off excess.<br>
                    <em>4.</em> Fry half of the wings at a time for about 5-7 minutes, until light golden. The oil will drop to about 320°F (160°C).<br>
                    <em>5.</em> Drain the wings and increase the heat. Heat oil to about 375°F (190°C).<br>
                    <em>6.</em> Fry the wings a second time until golden brown and crispy. Drain and set aside.<br>
                    <em>7.</em> In a pan, combine all the ingredients for the sauce and stir until bubbling. Toss in the wings.<br>
                    <em>8.</em> Nutrition Calories: 2805 Fat: 254 grams Carbs: 131 grams Fiber: 1 grams Sugars: 49 grams Protein: 8 grams<br>
                    <em>9.</em> Enjoy!</p>
