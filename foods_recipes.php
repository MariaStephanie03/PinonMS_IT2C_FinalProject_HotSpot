                <h3>Budae jjigae</h3>
                <p>Army Stew</p>
                <p><strong>Ingredients:</strong><br>
                    <em>1.</em> 3 pounds meaty beef short ribs<br>
                    <em>2.</em> 1/2 onion<br>
                    <em>3.</em> 3 - 4 thin ginger slices (about 1-inch rounds)<br>
                    <em>4.</em> 5 garlic cloves<br>
                    <em>5.</em> 2 white parts of scallions</p>

                <p><strong>Braising Liquid:</strong><br>
                    <em>1.</em> 1/2 cup soy sauce<br>
                    <em>2.</em> 2 tablespoons sugar 3 tablespoons for sweeter ribs
                    <em>3.</em> 2 tablespoons honey
                    <em>4.</em> 1/4 cup rice wine or mirin
                    <em>5.</em> 1/2 medium Asian pear grated (about 1/2 cup)
                    <em>6.</em> 1/2 small onion grated
                    <em>7.</em> 1 tablespoon minced garlic
                    <em>8.</em> 1 teaspoon grated or juiced ginger
                    <em>9.</em> 1/2 teaspoon black pepper
                    <em>10.</em>2 tablespoons sesame oil</p>

                <p><strong>Vegetables:</strong><br>
                    <em>1.</em> 10 ounces Korean radish cut into large chunks (about 1-1/2-inch cubes)<br>
                    <em>2.</em> 3 - 4 dried shiitake mushrooms soaked and quartered<br>
                    <em>3.</em> 1 large carrot cut into large chunks (about 1 inch long)<br>
                    <em>4.</em> 2 green parts of scallion</p>

                <p><strong>Optional garnishes:</strong><br>
                    <em>1.</em> 6 - 8 peeled fresh chestnuts<br>
                    <em>2.</em> 6 dried Korean dates/jujube<br>
                    <em>3.</em> 10 - 12 gingko nuts or 1 tablespoon pine nuts</p>

                <p><strong>Instructions:</strong><br>
                    <em>1.</em> Trim off any excess fat. Rinse the ribs and let them sit in cold water for about 30 minutes to draw out excess blood. Drain.<br>
                    <em>2.</em> In a large pot, bring 5 cups of water to a boil over high heat along with the onion, ginger, garlic and scallion white parts. Add the short ribs, and bring it to a boil, uncovered, skimming off the foam. Reduce the heat to medium high and cook for about 10 minutes. Remove the ribs to a plate, reserving the stock.<br>
                    <em>3.</em> Strain the stock, and skim off the fat. (Refrigerating the stock will help the fat solidify for easier removal. If you have room in the freezer, the process will go faster.)<br>
                    <em>4.</em> Mix together all the braising liquid ingredients, except the sesame oil. Return the ribs to the pot. Pour the braising liquid over the meat and stir. (You can prepare the ribs up to this point the day before, and finish braising when you need to serve.)<br>
                    <em>5.</em> Meanwhile, prepare the vegetables and optional garnish.<br>
                    <em>6.</em> Add 2-1/2 cups of the reserved stock to the ribs. Cover and bring it to a boil over medium heat. Continue to boil for 20 to 30 minutes, depending on the thickness of the ribs. Stir in the radish, mushrooms, and carrots. Continue to boil, covered, for about 20 minutes.<br>
                    <em>7.</em> Stir in the optional garnish ingredients. Return to boil, uncovered this time, for an additional 10 minutes, or until the sauce is thicker and the meat is tender but not falling apart. Add the green parts of scallions and the sesame oil and stir gently before turning the heat off.</p>
