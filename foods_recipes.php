                <h3>Buldak</h3>
                <p>Fire Chicken</p>
                <p><strong>Ingredients</strong><br>

                    <strong>For Chicken</strong><br>

                    <em>1.</em> 6 (de-boned)/2 Chicken drum sticks/ Chicken breast<br>
                    <em>2.</em> 2 Tbsp Soy sauce<br>
                    <em>3.</em> 1 Tbsp Sugar<br>
                    <em>4.</em> 1 Tbsp Traditional corn syrup (substitution honey)<br>
                    <em>5.</em> 2 Tbsp Cheong ju (clear rice wine similar to Japanese sake)<br>
                    <em>6.</em> 1 stalk Green onion<br>
                    <em>7.</em> To taste Black pepper<br>
                    <em>8.</em> As preference Olive oil<br>
                    <em>9.</em> As preference Sesame seeds (optional), roasted<br></p>

                    <p><strong>For marinating sauce:</strong><br>

                    <em>10.</em> 3 Tbsp Gochugaru (red chili pepper flakes)<br>
                    <em>11.</em> 2 Alapenos<br>
                    <em>12.</em> 1/2 Cup Korean pear (substitution asian pear)<br>
                    <em>13.</em> 1/4 Onion<br>
                    <em>14.</em> 3 Tbsp Garlic<br>
                    <em>15.</em> 2 Tbsp Soy sauce<br>
                    <em>16.</em> 1 tsp Spicy yellow mustard<br>
                    <em>17.</em> 1 Tbsp Sesame oil<br>
                    <em>18.</em> 1 Tbsp Sugar<br>
                    <em>19.</em> 1 Tbsp Mul yut (substitution honey)</p>

                <p><strong>Directions:</strong><br>

                    <em>1.</em>  Rinse chicken drum sticks in cold water and de-done meat with a sharp knife. Cut into 4 even pieces per leg drum -OR- rinse chicken breasts in cold water and cut into bite-size pieces.<br>

                    <em>2.</em>  In a large bowl, mix chicken with soy sauce, sugar, mul yut (substitution honey), cheong ju and ground pepper.<br>

                    <em>3.</em>  Marinate in refrigerator for 30 minutes.<br>

                    <em>4.</em>  Puree all marinating sauce ingredients together in a blender. Once completed, leave aside for later use.<br>

                    <em>5.</em>  On a non-stick frying pan or skillet, cook prepared chicken over medium heat until meat is just short of desired completion.<br>

                    <em>6.</em>  Take out chicken only and leave excess ingredients in frying pan or skillet.<br>

                    <em>7.</em>  In a large bowl, thoroughly mix chicken with blended sauce from steps 2 & 3.<br>

                    <em>8.</em>  In the same frying pan or skillet, add olive oil to taste and cook for an additional 3-4 minutes in medium high hea<br>

                    <em>9.</em>  Serve hot on plate.</p>
